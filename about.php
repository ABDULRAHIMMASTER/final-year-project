    <H3><B>Computerized revenue generation system is a computer-based system starting from compilation of list from receipt issuing to those paying, complex calculation involved to produce rate according to size of shop and complex. This project will study and analyze the concept and operations of revenue generation process as it is carried out central market shop management. The existing manual system towards the compilation of revenue and the monitoring unit makes it look difficult and boring for people under revenue generation department in Central Market Shop. With these, it can bring about fraudulency and delay development. The task of revenue generation in central market is a bulky one, in the sense that it consumes a lot of space, starting from compilation of list from receipt issuing to those paying, complex calculation involved to produce rate according to income earning or size of an organization is never an easy task to work on. Revenue generation is a means through which government establishment hunt for money, which will be accountable to the government purse for executing its projects, and the money collected from different areas like market, advertising unit, road transportation etc. are being monitored by a group of people referred to as the monitoring unit. The government in this case, expects members of the society to contribute to the affairs of the nation</B>.</H3>

    <H2 align="center"><b>Aim and Objectives of the study</b></H2>
<p><b>The primary aim of this project work is to look into the operations of revenue generation system in Fatimah Baika Central Market Katsina State and come out with a computerized design that will be more effective.</b></p>
<p><b>The specific objectives include:</b></p>
<ol><b>
<li>	To enhance revenue collection by Implementing streamline and automated revenue collection processes to increase the efficiency and accuracy of revenue collection in Fatimah Baika Central Market Katsina State.</li>
<li>	To Diversify revenue streams through Identifying and exploring new revenue sources and opportunities to reduce dependency on a single revenue stream, thus ensuring a more resilient and sustainable financial position.</li>
<li>	To make financial report transparent by establishing a system that promotes accountability, helps in tracking revenue inflows, and ensures that funds are allocated appropriately for developmental projects in the state.</li>
<li>	To Improved tax compliance by implementing strategies to enhance tax compliance among businesses and individuals, reducing tax evasion and improving overall revenue generation from taxation.</li>
</b></ol>
